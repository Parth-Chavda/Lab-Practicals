//Program_2 : Print address using single and multiple println.

public class Address
{
    public static void main(String[] args)
    {
        System.out.println("Using single println : \nGokulnagar,\nJamnagar, 361004,\nGujarat");
        System.out.println("Using multiple println : ");
        System.out.println("Gokulnagar,");
        System.out.println("Jamnagar, 361004");
        System.out.println("Gujarat");
    }
}
