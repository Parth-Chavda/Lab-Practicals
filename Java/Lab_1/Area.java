//Program_5 : Find area of circle.

public class Area
{
    public static void main(String[] args)
    {   
        float pi=3.14f;
        float r=5f; //r=radius
        float a=pi*r*r; //a=area 

        System.out.println("Radius of circle : "+r+"\nArea of circle : "+a);                  
    }
}
